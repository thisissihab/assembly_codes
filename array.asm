DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
ARR DB 48,49,51,50
TMP DB 0

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV CX, 4
    MOV SI, OFFSET ARR
    MOV AH, 2
    PRINT:
        MOV DX, [SI]

        INT 21H
        INC SI

        MOV DX, ' '
        INT 21H

        LOOP PRINT

    MOV AH, 4CH
    INT 21H

MAIN ENDP
END MAIN